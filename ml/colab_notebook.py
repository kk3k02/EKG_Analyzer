def bandpass_filter(sig, lowcut=0.5, highcut=40.0, fs=FS, order=4):
    nyq = 0.5 * fs
    b, a = butter(order, [lowcut/nyq, highcut/nyq], btype='band')
    return filtfilt(b, a, sig)

def extract_features(segment, fs=FS):
    """
    Wektor cech łączący:
    - 4 pasma PSD (Welch) znormalizowane do total power
    - mean/std PSD
    - 60 współczynników FFT (amplituda + faza)
    - statystyki czasowe: mean, std, skewness, kurtosis, RMS, ZCR
    """
    from scipy.stats import skew, kurtosis as kurt

    # PSD bands
    freqs, psd = welch(segment, fs=fs, nperseg=512)
    total_power = np.trapz(psd, freqs) + 1e-10
    features = []
    for lo, hi in [(0.04,0.15),(0.15,0.4),(0.5,5),(5,15),(15,40)]:
        idx = (freqs >= lo) & (freqs <= hi)
        bp  = np.trapz(psd[idx], freqs[idx]) if np.any(idx) else 0
        features.append(bp / total_power)
    features += [np.mean(psd), np.std(psd), np.max(psd),
                 freqs[np.argmax(psd)]]   # dominant frequency

    # FFT magnitude (pierwsze 60 binów) + faza
    fft_vals = fft(segment)
    mag  = np.abs(fft_vals)[:60]
    mag /= (np.max(mag) + 1e-10)
    phase = np.angle(fft_vals)[:60]
    features.extend(mag.tolist())
    features.extend(phase.tolist())

    # Time-domain
    features += [
        np.mean(segment),
        np.std(segment),
        float(skew(segment)),
        float(kurt(segment)),
        np.sqrt(np.mean(segment**2)),          # RMS
        np.sum(np.diff(np.sign(segment)) != 0) / WINDOW_LEN,  # ZCR
    ]

    return np.array(features, dtype=np.float32)


