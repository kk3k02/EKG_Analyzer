class WindowCNN(nn.Module):
    """
    1D CNN na wektorze cech FFT+PSD+time-domain.
    Residual connections stabilizują trening na krótkich wektorach.
    """
    def __init__(self, input_dim, n_classes):
        super().__init__()
        self.input_bn = nn.BatchNorm1d(1)

        self.block1 = nn.Sequential(
            nn.Conv1d(1, 64, kernel_size=7, padding=3),
            nn.BatchNorm1d(64), nn.GELU(), nn.Dropout(0.2),
            nn.Conv1d(64, 64, kernel_size=5, padding=2),
            nn.BatchNorm1d(64), nn.GELU(),
        )
        self.pool1 = nn.MaxPool1d(2)

        self.block2 = nn.Sequential(
            nn.Conv1d(64, 128, kernel_size=5, padding=2),
            nn.BatchNorm1d(128), nn.GELU(), nn.Dropout(0.2),
            nn.Conv1d(128, 128, kernel_size=3, padding=1),
            nn.BatchNorm1d(128), nn.GELU(),
        )
        self.pool2 = nn.MaxPool1d(2)

        self.block3 = nn.Sequential(
            nn.Conv1d(128, 256, kernel_size=3, padding=1),
            nn.BatchNorm1d(256), nn.GELU(), nn.Dropout(0.3),
        )
        self.gap = nn.AdaptiveAvgPool1d(1)

        self.fc = nn.Sequential(
            nn.Flatten(),
            nn.Linear(256, 128), nn.GELU(), nn.Dropout(0.4),
            nn.Linear(128, 64),  nn.GELU(),
            nn.Linear(64, n_classes),
        )

    def forward(self, x):
        x = x.unsqueeze(1)           # (B, 1, input_dim)
        x = self.input_bn(x)
        x = self.pool1(self.block1(x))
        x = self.pool2(self.block2(x))
        x = self.gap(self.block3(x))
        return self.fc(x)


