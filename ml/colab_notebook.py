def process_one_record(rec_name, db, forced_label=None):
    rec_path = os.path.join(DATA_DIR, rec_name)
    try:
        rec = wfdb.rdrecord(rec_path)
    except:
        try:
            rec = wfdb.rdrecord(rec_name, pn_dir=db)
        except Exception as e:
            print(f'  ERR {rec_name}: {e}')
            return None

    sig = np.nan_to_num(rec.p_signal[:, 0].astype(np.float64))
    try:
        nyq  = 0.5 * FS
        b, a = butter(4, [0.5/nyq, 40.0/nyq], btype='band')
        sig  = filtfilt(b, a, sig)
    except:
        pass

    ann = None
    if forced_label is None:
        try:
            ann = wfdb.rdann(rec_path, 'atr')
        except:
            try:
                ann = wfdb.rdann(rec_name, 'atr', pn_dir=db)
            except:
                pass

    starts = np.arange(0, len(sig) - WINDOW_LEN + 1, STEP_LEN)
    if len(starts) == 0:
        return None

    windows = [sig[s:s+WINDOW_LEN].astype(np.float32) for s in starts]

    if forced_label is not None:
        labels = [forced_label] * len(starts)
    elif ann is not None:
        ann_s  = np.array(ann.sample)
        ann_sy = np.array(ann.symbol)
        labels = []
        for s in starts:
            mask  = (ann_s >= s) & (ann_s < s + WINDOW_LEN)
            syms  = ann_sy[mask].tolist()
            valid = [x for x in syms if x in ARR_SYMBOLS | NSR_SYMBOLS]
            if not valid:
                labels.append(None)
                continue
            arr_n = sum(1 for x in valid if x in ARR_SYMBOLS)
            labels.append('ARR' if arr_n >= 1 else 'NSR')
    else:
        return None

    keep = [i for i, l in enumerate(labels) if l is not None]
    if not keep:
        return None

    windows_np = np.stack([windows[i] for i in keep])
    labels_f   = [labels[i] for i in keep]
    features   = extract_features_batch(windows_np)

    return features, labels_f, rec_name


