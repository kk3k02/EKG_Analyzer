def load_model(weights_path, device=None):
    if device is None:
        device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')

    ckpt = torch.load(weights_path, map_location=device, weights_only=True)

    sklearn_path = weights_path.replace('_weights.pt', '_sklearn.pkl')
    with open(sklearn_path, 'rb') as f:
        sk = pickle.load(f)

    meta_path = weights_path.replace('_weights.pt', '_meta.json')
    with open(meta_path) as f:
        meta = json.load(f)

    net = WindowCNN(
        input_dim=ckpt['model_config']['input_dim'],
        n_classes=ckpt['model_config']['n_classes'],
    ).to(device)
    net.load_state_dict(ckpt['model_state'])
    net.eval()

    return {
        'net':        net,
        'scaler':     sk['scaler'],
        'le':         sk['le'],
        'device':     device,
        'fs':         meta['fs'],
        'window_len': meta['window_len'],
        'step_len':   meta['step_len'],
        'classes':    meta['classes'],
    }


def predict_signal(signal_1d, model_dict, return_windows=False):
    md = model_dict
    sig = np.nan_to_num(signal_1d.astype(np.float64))

    try:
        nyq = 0.5 * md['fs']
        b, a = butter(4, [0.5/nyq, 40.0/nyq], btype='band')
        sig = filtfilt(b, a, sig)
    except Exception:
        pass

    starts = np.arange(0, len(sig) - md['window_len'] + 1, md['step_len'])
    if len(starts) == 0:
        raise ValueError(
            f'Sygnał za krótki – potrzeba min {md["window_len"]} próbek '
            f'({md["window_len"]/md["fs"]:.1f}s przy fs={md["fs"]}Hz)'
        )

    windows = np.stack([sig[s:s+md['window_len']] for s in starts]).astype(np.float32)
    feats = extract_features_batch(windows, fs=md['fs'])
    feats_sc = md['scaler'].transform(feats)

    with torch.no_grad():
        logits = md['net'](torch.tensor(feats_sc).to(md['device']))
        probs = torch.softmax(logits, 1).cpu().numpy()
        preds = logits.argmax(1).cpu().numpy()

    pred_labels = md['le'].inverse_transform(preds)
    counts = Counter(pred_labels)
    total = len(pred_labels)
    majority = counts.most_common(1)[0][0]
    maj_idx = md['le'].transform([majority])[0]
    maj_prob = float(probs[preds == maj_idx, maj_idx].mean()) if (preds == maj_idx).any() else 0.0

    result = {
        'majority_class': majority,
        'majority_prob': round(maj_prob, 3),
        'class_distribution': {k: round(v/total*100, 1) for k, v in counts.items()},
        'n_windows': total,
    }

    if return_windows:
        result['window_results'] = [
            {
                'start_s': float(s / md['fs']),
                'end_s': float((s + md['window_len']) / md['fs']),
                'class': pred_labels[i],
                'probs': dict(zip(md['classes'], probs[i].round(3).tolist())),
            }
            for i, s in enumerate(starts)
        ]

    return result


