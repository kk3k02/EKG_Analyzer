def process_records_split(records, db, label, max_records):
    data = []

    for rec in records[:max_records]:
        record = load_record(rec, db)
        if record is None:
            continue

        sig = np.nan_to_num(record.p_signal[:,0])

        try:
            sig = bandpass_filter(sig)
        except:
            pass

        n_seg = min(N_SEGMENTS_PER_RECORD, len(sig)//SEGMENT_LEN)

        rec_features = []
        for i in range(n_seg):
            seg = sig[i*SEGMENT_LEN:(i+1)*SEGMENT_LEN]
            if len(seg) == SEGMENT_LEN:
                rec_features.append(extract_features(seg))

        if len(rec_features) > 0:
            data.append((np.array(rec_features), label))

        print(f'✔ {rec} -> {len(rec_features)} segmentów')

    return data

