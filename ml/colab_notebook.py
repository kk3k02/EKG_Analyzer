# Symbole annotacji wg standardu AAMI/MIT-BIH
ARR_BEATS = set(['V', 'F', 'f', 'A', 'a', 'J', 'S', 'E', 'j', 'n', 'e', 'Q'])
NSR_BEATS = set(['N', 'L', 'R', 'B', '/', 'p'])
# CHF — baza chfdb nie ma beat-level annotacji, weryfikujemy tylko ARR i NSR

def classify_annotation(ann_symbols):
    """
    Dla okna: jeśli > 10% uderzeń to annotacje arytmii → ARR, inaczej NSR.
    Zwraca etykietę lub None jeśli brak annotacji.
    """
    if len(ann_symbols) == 0:
        return None
    arr_count = sum(1 for s in ann_symbols if s in ARR_BEATS)
    ratio = arr_count / len(ann_symbols)
    return 'ARR' if ratio > 0.10 else 'NSR'


# =========================
# FEATURE EXTRACTION (identyczna jak przy treningu!)
# =========================
def bandpass_filter(data, lowcut=0.5, highcut=40.0, fs=FS, order=4):
    nyq = 0.5 * fs
    b, a = butter(order, [lowcut/nyq, highcut/nyq], btype='band')
    return filtfilt(b, a, data)

def extract_features(segment):
    freqs, psd = welch(segment, fs=FS, nperseg=512)
    total_power = np.trapz(psd, freqs) + 1e-10
    features = []
    for lo, hi in [(0.04,0.15),(0.15,0.4),(0.5,5),(5,15)]:
        idx = (freqs >= lo) & (freqs <= hi)
        bp = np.trapz(psd[idx], freqs[idx]) if np.any(idx) else 0
        features.append(bp / total_power)
    features += [np.mean(psd), np.std(psd)]
    fft_vals = np.abs(fft(segment))[:30]
    fft_vals /= (np.max(fft_vals) + 1e-10)
    features.extend(fft_vals.tolist())
    return np.array(features, dtype=np.float32)


# =========================
# SLIDING WINDOW INFERENCE NA JEDNYM REKORDZIE
# =========================
def sliding_window_inference(record_path, model, scaler, le, db=None):
    """
    Wczytuje rekord, przepuszcza przez CNN okno po oknie.
    Zwraca:
        windows      — lista słowników {start_s, end_s, pred_label, pred_probs}
        ann_labels   — lista ground-truth etykiet per okno (lub None)
    """
    # Wczytaj sygnał
    try:
        rec = wfdb.rdrecord(record_path)
    except:
        rec = wfdb.rdrecord(record_path, pn_dir=db)

    sig = np.nan_to_num(rec.p_signal[:, 0])
    try:
        sig = bandpass_filter(sig)
    except:
        pass

    # Wczytaj annotacje jeśli istnieją
    ann = None
    try:
        ann = wfdb.rdann(record_path, 'atr')
    except:
        try:
            ann = wfdb.rdann(record_path, 'atr', pn_dir=db)
        except:
            pass  # baza CHF nie ma annotacji

    model.eval()
    windows = []
    ann_labels = []

    n_windows = (len(sig) - WINDOW_LEN) // STEP_LEN + 1

    for i in range(n_windows):
        start = i * STEP_LEN
        end   = start + WINDOW_LEN
        if end > len(sig):
            break

        segment = sig[start:end]

        # FFT feature extraction
        feat = extract_features(segment)
        feat_sc = scaler.transform(feat.reshape(1, -1))
        x = torch.tensor(feat_sc, dtype=torch.float32).to(DEVICE)

        with torch.no_grad():
            logits = model(x)
            probs  = torch.softmax(logits, dim=1).cpu().numpy()[0]
            pred_idx = logits.argmax(1).item()
            pred_label = le.inverse_transform([pred_idx])[0]

        windows.append({
            'start_s':    start / FS,
            'end_s':      end   / FS,
            'pred_label': pred_label,
            'pred_probs': dict(zip(le.classes_, probs.round(3))),
        })

        # Ground truth z annotacji
        if ann is not None:
            mask = (ann.sample >= start) & (ann.sample < end)
            syms = [ann.symbol[j] for j in range(len(ann.symbol)) if mask[j]]
            gt = classify_annotation(syms)
        else:
            gt = None
        ann_labels.append(gt)

    return windows, ann_labels


