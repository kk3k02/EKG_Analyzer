_BP_BANDS = [(0.04,0.15),(0.15,0.4),(0.5,5),(5,15),(15,40)]

def extract_features_batch(windows_2d, fs=FS):
    """
    windows_2d: (N, WINDOW_LEN) float32
    Zwraca:    (N, n_features) float32
    Wszystkie operacje zwektoryzowane po osi 0.
    """
    N = len(windows_2d)

    # --- FFT (wszystkie okna naraz) ---
    fft_vals  = np.fft.rfft(windows_2d, axis=1)          # (N, WINDOW_LEN//2+1)
    mag_full  = np.abs(fft_vals)                          # (N, WINDOW_LEN//2+1)
    mag60     = mag_full[:, :60]
    mag60    /= (mag60.max(axis=1, keepdims=True) + 1e-10)
    phase60   = np.angle(fft_vals[:, :60])

    # --- Welch PSD (batch przez pętlę — welch nie jest wektorowy, ale liczymy raz) ---
    freqs, _ = welch(windows_2d[0], fs=fs, nperseg=512)
    psds = np.zeros((N, len(freqs)), dtype=np.float32)
    for i, w in enumerate(windows_2d):
        _, psds[i] = welch(w, fs=fs, nperseg=512)

    total_power = np.trapz(psds, freqs, axis=1) + 1e-10   # (N,)

    band_feats = []
    for lo, hi in _BP_BANDS:
        idx = (freqs >= lo) & (freqs <= hi)
        bp  = np.trapz(psds[:, idx], freqs[idx], axis=1)
        band_feats.append(bp / total_power)
    band_feats = np.stack(band_feats, axis=1)              # (N, 5)

    dom_freq_idx = np.argmax(psds, axis=1)
    dom_freq     = freqs[dom_freq_idx]                     # (N,)

    psd_mean = psds.mean(axis=1)
    psd_std  = psds.std(axis=1)
    psd_max  = psds.max(axis=1)

    # --- Time domain (wektorowo) ---
    td_mean = windows_2d.mean(axis=1)
    td_std  = windows_2d.std(axis=1)
    td_rms  = np.sqrt((windows_2d**2).mean(axis=1))

    # skewness i kurtosis — scipy obsługuje axis
    td_skew = skew(windows_2d, axis=1)
    td_kurt = kurt(windows_2d, axis=1)

    signs   = np.sign(windows_2d)
    zcr     = (np.diff(signs, axis=1) != 0).sum(axis=1) / WINDOW_LEN

    # --- Składanie wektora ---
    features = np.concatenate([
        band_feats,                          # 5
        psd_mean[:,None], psd_std[:,None],   # 2
        psd_max[:,None], dom_freq[:,None],   # 2
        mag60,                               # 60
        phase60,                             # 60
        td_mean[:,None], td_std[:,None],     # 2
        td_skew[:,None], td_kurt[:,None],    # 2
        td_rms[:,None],  zcr[:,None],        # 2
    ], axis=1)                               # = 135 cech

    return features.astype(np.float32)


