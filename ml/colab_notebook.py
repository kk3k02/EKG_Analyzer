def infer_record(rec_name, db, model, scaler, le):
    rec_path = os.path.join(DATA_DIR, rec_name)
    try:
        rec = wfdb.rdrecord(rec_path)
    except:
        rec = wfdb.rdrecord(rec_name, pn_dir=db)

    sig = np.nan_to_num(rec.p_signal[:, 0].astype(np.float64))
    try:
        nyq  = 0.5 * FS
        b, a = butter(4, [0.5/nyq, 40.0/nyq], btype='band')
        sig  = filtfilt(b, a, sig)
    except:
        pass

    ann = None
    try:
        ann = wfdb.rdann(rec_path, 'atr')
    except:
        pass

    starts   = np.arange(0, len(sig) - WINDOW_LEN + 1, STEP_LEN)
    windows  = np.stack([sig[s:s+WINDOW_LEN] for s in starts]).astype(np.float32)
    feats    = extract_features_batch(windows)
    feats_sc = scaler.transform(feats)

    model.eval()
    with torch.no_grad():
        logits = model(torch.tensor(feats_sc).to(DEVICE))
        probs  = torch.softmax(logits, 1).cpu().numpy()
        preds  = logits.argmax(1).cpu().numpy()

    results = []
    for i, (s, pred_idx) in enumerate(zip(starts, preds)):
        gt = None
        if ann is not None:
            mask  = (np.array(ann.sample) >= s) & (np.array(ann.sample) < s + WINDOW_LEN)
            syms  = np.array(ann.symbol)[mask].tolist()
            valid = [x for x in syms if x in ARR_SYMBOLS | NSR_SYMBOLS]
            if valid:
                arr_n = sum(1 for x in valid if x in ARR_SYMBOLS)
                gt    = 'ARR' if arr_n >= 1 else 'NSR'

        results.append({
            'start_s':    s / FS,
            'end_s':      (s + WINDOW_LEN) / FS,
            'pred':       le.classes_[pred_idx],
            'gt':         gt,
            'probs':      dict(zip(le.classes_, probs[i].round(3))),
            'correct':    (le.classes_[pred_idx] == gt) if gt else None,
        })

    return results


