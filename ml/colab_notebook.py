train_dl = DataLoader(TensorDataset(X_tr_t, y_tr_t), batch_size=32, shuffle=True)
test_dl  = DataLoader(TensorDataset(X_te_t, y_te_t), batch_size=64, shuffle=False)

n_classes = len(classes)
cnn = SpectralCNN(X_train_sc.shape[1], n_classes).to(DEVICE)
print(f'Parametry CNN: {sum(p.numel() for p in cnn.parameters()):,}')

criterion = nn.CrossEntropyLoss()
optimizer = optim.Adam(cnn.parameters(), lr=1e-3, weight_decay=1e-4)
scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=50)
EPOCHS = 50
history = {'train_loss': [], 'train_acc': [], 'val_loss': [], 'val_acc': []}

print(f'Trening przez {EPOCHS} epok...')
for epoch in range(EPOCHS):
    cnn.train()
    tl, tc, tt = 0., 0, 0
    for xb, yb in train_dl:
        xb, yb = xb.to(DEVICE), yb.to(DEVICE)
        optimizer.zero_grad()
        logits = cnn(xb)
        loss = criterion(logits, yb)
        loss.backward(); optimizer.step()
        tl += loss.item() * len(xb)
        tc += (logits.argmax(1) == yb).sum().item(); tt += len(xb)
    scheduler.step()
    cnn.eval()
    vl, vc, vt = 0., 0, 0
    with torch.no_grad():
        for xb, yb in test_dl:
            xb, yb = xb.to(DEVICE), yb.to(DEVICE)
            logits = cnn(xb)
            loss = criterion(logits, yb)
            vl += loss.item() * len(xb)
            vc += (logits.argmax(1) == yb).sum().item(); vt += len(xb)
    history['train_loss'].append(tl/tt); history['train_acc'].append(tc/tt)
    history['val_loss'].append(vl/vt);   history['val_acc'].append(vc/vt)
    if (epoch+1) % 10 == 0:
        print(f'Epoka {epoch+1:3d}/{EPOCHS} | '
              f'Loss {tl/tt:.4f}/{vl/vt:.4f} | '
              f'Acc {tc/tt*100:.1f}%/{vc/vt*100:.1f}%')

cnn.eval()
all_preds, all_probs = [], []
